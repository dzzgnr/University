t = int(input('Input minutes: '))  # вводимо кількість хвилин
# згідно із заданою в задачі умовою обраховуємо на яку хвилину приходиться який колір
# і вводимо оптимізовані значення в if-elif-else
if 1 <= (t % 6) <= 3:
    print('Green')
elif (t % 6) == 4:
    print('Yellow')
else:
    print('Red')
